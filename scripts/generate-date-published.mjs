#!/usr/bin/env node
/**
 * Generates src/data/datePublished.ts: a permalink → ISO-date map of each
 * doc's first git commit, consumed by the DocItem JSON-LD wrapper as
 * schema.org datePublished (dateModified already comes from Docusaurus's
 * git-based showLastUpdateTime).
 *
 * Runs automatically before `npm run build` (see the prebuild script).
 * Requires full git history — deploy.yml already checks out with
 * fetch-depth: 0. In a shallow clone the first-commit date would be wrong,
 * so we keep the committed map instead of regenerating.
 */
import {execFileSync} from 'node:child_process';
import {readdirSync, writeFileSync} from 'node:fs';
import {join, relative, sep} from 'node:path';

const repoRoot = join(import.meta.dirname, '..');
const docsDir = join(repoRoot, 'docs');
const outFile = join(repoRoot, 'src', 'data', 'datePublished.ts');

const git = (...args) =>
  execFileSync('git', args, {cwd: repoRoot, encoding: 'utf8'}).trim();

if (git('rev-parse', '--is-shallow-repository') === 'true') {
  console.warn(
    '[generate-date-published] shallow clone — keeping existing datePublished.ts',
  );
  process.exit(0);
}

const mdFiles = [];
(function walk(dir) {
  for (const entry of readdirSync(dir, {withFileTypes: true})) {
    const path = join(dir, entry.name);
    if (entry.isDirectory()) walk(path);
    else if (/\.mdx?$/.test(entry.name)) mdFiles.push(path);
  }
})(docsDir);

const map = {};
for (const file of mdFiles) {
  const rel = relative(repoRoot, file);
  // First commit that added the file (follows renames).
  const added = git(
    'log', '--follow', '--diff-filter=A', '--format=%aI', '--', rel,
  )
    .split('\n')
    .filter(Boolean)
    .at(-1);
  if (!added) continue; // untracked file — no publish date yet
  const permalink =
    '/docs/' +
    relative(docsDir, file).split(sep).join('/').replace(/\.mdx?$/, '');
  map[permalink] = added;
}

const sorted = Object.fromEntries(
  Object.entries(map).sort(([a], [b]) => a.localeCompare(b)),
);

writeFileSync(
  outFile,
  '// Generated by scripts/generate-date-published.mjs — do not edit by hand.\n' +
    '// Maps doc permalinks to the ISO date of the commit that added the file.\n' +
    `const DATE_PUBLISHED: Record<string, string> = ${JSON.stringify(sorted, null, 2)};\n` +
    'export default DATE_PUBLISHED;\n',
);
console.log(
  `[generate-date-published] wrote ${Object.keys(sorted).length} entries to ${relative(repoRoot, outFile)}`,
);
